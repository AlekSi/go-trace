type rwLimiter struct {
	w   io.Writer
	r   io.Reader
	n   int
	off int
	err error
}

func (l *rwLimiter) Write(p []byte) (int, error) {
	if off := l.off + len(p); off > l.n {
		return len(p) / 2, l.err
	}
	n, err := l.w.Write(p)
	l.off += n
	return n, err
}

func (l *rwLimiter) Read(p []byte) (int, error) {
	if off := l.off + len(p); off > l.n {
		return off - l.off, l.err
	}
	n, err := l.r.Read(p)
	l.off += n
	return n, err
}

